    return weightedLetters[_random.nextInt(weightedLetters.length)];
  }

  /// Get random categories for a game round
  static List<GameCategory> getRandomCategories(int count) {
    final shuffled = List<GameCategory>.from(allCategories);
    shuffled.shuffle(_random);
    return shuffled.take(count).toList();
  }

  /// Get localized category name
  static String getCategoryName(String categoryId, String language) {
    final category = allCategories.firstWhere(
      (c) => c.id == categoryId,
      orElse: () => allCategories.first,
    );
    return category.localizedNames[language] ?? category.name;
  }

  /// Validate if a word fits a category and starts with the given letter.
  /// Uses case-insensitive matching. Returns true if the answer is valid.
  static bool isValidAnswer(String word, String categoryId, String letter, String language) {
    final normalizedWord = word.toLowerCase().trim();
    final normalizedLetter = letter.toUpperCase().trim();

    if (normalizedWord.isEmpty) return false;
    if (!normalizedWord.startsWith(normalizedLetter.toLowerCase())) return false;

    final answers = getAnswersForCategory(categoryId, normalizedLetter, language);
    return answers.any((a) => a.toLowerCase() == normalizedWord);
  }

  /// Get valid answers for a category, letter, and language.
  static List<String> getAnswersForCategory(String categoryId, String letter, String language) {
    // Universal categories (same data, translated per language)
    final universalData = _universalCategories[categoryId];
    if (universalData != null) {
      final langData = universalData[language] ?? universalData['en'];
      if (langData != null) {
        return langData[letter.toUpperCase()] ?? [];
      }
    }

    // Language-specific categories
    final langSpecific = _languageSpecificCategories[language];
    if (langSpecific != null) {
      final catData = langSpecific[categoryId];
      if (catData != null) {
        return catData[letter.toUpperCase()] ?? [];
      }
    }

    return [];
  }

  /// Get a hint for an answer (first 2-3 characters revealed)
  static String? getHintForCategory(String categoryId, String letter, String language) {
    final answers = getAnswersForCategory(categoryId, letter, language);
    if (answers.isEmpty) return null;
    final answer = answers[_random.nextInt(answers.length)];
    final hintLength = answer.length <= 4 ? 2 : 3;
    return '${answer.substring(0, hintLength)}...';
  }

