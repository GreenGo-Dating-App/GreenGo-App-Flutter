// ============================================================
// VOCABULARY DICTIONARY
// Comprehensive word sets per language for local word validation.
// Used by Word Bomb, Vocabulary Chain, and general word existence checks.
// All words are lowercase for O(1) Set lookup.
//
// Base ~2000 words are inline below. Extended vocabularies (5000-11000
// words each) are imported from per-language files and merged lazily
// on first access.
// ============================================================

class VocabularyDictionary {
  VocabularyDictionary._();

  static final _random = Random();

  /// Lazily-built merged dictionaries (base + extended per language)
  static Map<String, Set<String>>? _merged;

  static Map<String, Set<String>> get _dictionaries {
    return _merged ??= {
      'en': {..._enWords, ...VocabularyEn.words},
      'es': {..._esWords, ...VocabularyEs.words},
      'fr': {..._frWords, ...VocabularyFr.words},
      'de': {..._deWords, ...VocabularyDe.words},
      'it': {..._itWords, ...VocabularyIt.words},
      'pt': {..._ptWords, ...VocabularyPt.words},
      'pt-BR': {..._ptBrWords, ...VocabularyPtBr.words},
    };
  }

  /// Check if a word exists in the given language
  static bool isValidWord(String word, String language) {
    final dict = _dictionaries[language];
    if (dict == null) return false;
    return dict.contains(word.toLowerCase().trim());
  }

  /// Get a random word from the dictionary for a given language
  static String getRandomWord(String language) {
    final dict = _dictionaries[language];
    if (dict == null || dict.isEmpty) return '';
    final list = dict.toList();
    return list[_random.nextInt(list.length)];
  }

  /// Get words that start with a given prefix
  static List<String> getWordsStartingWith(String prefix, String language, {int limit = 20}) {
    final dict = _dictionaries[language];
    if (dict == null) return [];
    final lowerPrefix = prefix.toLowerCase().trim();
    return dict
        .where((w) => w.startsWith(lowerPrefix))
        .take(limit)
        .toList();
  }

  /// Get words that contain a given substring
  static List<String> getWordsContaining(String substring, String language, {int limit = 20}) {
    final dict = _dictionaries[language];
    if (dict == null) return [];
    final lowerSub = substring.toLowerCase().trim();
    return dict
        .where((w) => w.contains(lowerSub))
        .take(limit)
        .toList();
  }

  /// Get total word count for a language
  static int getWordCount(String language) {
    return _dictionaries[language]?.length ?? 0;
  }

