/// A translation entry with the word, its translations, and meaning
class TranslationEntry {
  final String word;
  final String meaning;
  final int difficulty; // 1-10

  const TranslationEntry({
    required this.word,
    required this.meaning,
    this.difficulty = 1,
  });
}

class TranslationDictionary {
  TranslationDictionary._();

  static final _random = Random();

  /// Check if a translation is correct (with optional fuzzy matching)
  static bool isCorrectTranslation(
    String word,
    String translation,
    String fromLang,
    String toLang, {
    int tolerance = 1,
  }) {
    final translations = getTranslations(word, fromLang, toLang);
    if (translations.isEmpty) return false;

    final normalizedTranslation = translation.toLowerCase().trim();

    for (final correct in translations) {
      final normalizedCorrect = correct.toLowerCase().trim();
      if (normalizedTranslation == normalizedCorrect) return true;

      // Fuzzy match with Levenshtein distance
      if (tolerance > 0) {
        final distance = _levenshteinDistance(normalizedTranslation, normalizedCorrect);
        if (distance <= tolerance) return true;
      }
    }

    return false;
  }

  /// Get all valid translations for a word from one language to another.
  /// Returns empty list if no translation found.
  static List<String> getTranslations(String word, String fromLang, String toLang) {
    final normalizedWord = word.toLowerCase().trim();

    // Direct lookup: English -> target language
    if (fromLang == 'en') {
      final langPairs = _translationPairs[toLang];
      if (langPairs != null) {
        final entry = langPairs[normalizedWord];
        if (entry != null) return entry;
      }
      return [];
    }

    // Reverse lookup: target language -> English
    if (toLang == 'en') {
      final langPairs = _translationPairs[fromLang];
      if (langPairs != null) {
        final results = <String>[];
        langPairs.forEach((enWord, translations) {
          if (translations.any((t) => t.toLowerCase() == normalizedWord)) {
            results.add(enWord);
          }
        });
        return results;
      }
      return [];
    }

    // Between two non-English languages: go through English as bridge
    // First find the English word for the source
    final sourcePairs = _translationPairs[fromLang];
    if (sourcePairs == null) return [];

    String? englishWord;
    sourcePairs.forEach((enWord, translations) {
      if (translations.any((t) => t.toLowerCase() == normalizedWord)) {
        englishWord = enWord;
      }
    });

    if (englishWord == null) return [];

    // Then find the target translation
    final targetPairs = _translationPairs[toLang];
    if (targetPairs == null) return [];

    return targetPairs[englishWord!] ?? [];
  }

  /// Get the meaning/definition of a word for educational display.
  /// Returns null if no meaning is found.
  static String? getMeaning(String word, String language) {
    final normalizedWord = word.toLowerCase().trim();

    if (language == 'en') {
      return _englishMeanings[normalizedWord];
    }

    // For other languages, find the English translation and return its meaning
    final langPairs = _translationPairs[language];
    if (langPairs == null) return null;

    String? englishWord;
    langPairs.forEach((enWord, translations) {
      if (translations.any((t) => t.toLowerCase() == normalizedWord)) {
        englishWord = enWord;
      }
    });

    if (englishWord != null) {
      return _englishMeanings[englishWord!.toLowerCase()];
    }

    return null;
  }

  /// Get a random translation pair for a given target language and difficulty.
  /// Returns (english, translation, meaning) or null if no pairs available.
  static ({String english, String translation, String meaning})? getRandomPair(
    String targetLang, {
    int? maxDifficulty,
  }) {
    final langPairs = _translationPairs[targetLang];
    if (langPairs == null || langPairs.isEmpty) return null;

    final entries = langPairs.entries.toList();
    if (entries.isEmpty) return null;

    // Filter by difficulty if specified
    List<MapEntry<String, List<String>>> filtered = entries;
    if (maxDifficulty != null) {
      final diffEntries = _wordDifficulties;
      filtered = entries.where((e) {
        final diff = diffEntries[e.key] ?? 1;
        return diff <= maxDifficulty;
      }).toList();
      if (filtered.isEmpty) filtered = entries;
    }

    final entry = filtered[_random.nextInt(filtered.length)];
    final translation = entry.value[_random.nextInt(entry.value.length)];
    final meaning = _englishMeanings[entry.key] ?? '';

    return (english: entry.key, translation: translation, meaning: meaning);
  }

  /// Get word difficulty (1-10)
  static int getWordDifficulty(String word) {
    return _wordDifficulties[word.toLowerCase().trim()] ?? 3;
  }

  /// Get all available English words with translations for a language
  static List<String> getAvailableWords(String targetLang) {
    return _translationPairs[targetLang]?.keys.toList() ?? [];
  }

